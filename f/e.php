    <?php
    $a = 1; // กำหนดค่าเริ่มต้น

    while ($a <= 10) { // ตรวจสอบเงื่อนไขว่ายังน้อยกว่าหรือเท่ากับ 10 หรือไม่
        
        // แสดงลำดับที่และชื่อ
        echo "$a : จิราภา บุญสมยา(นิ้ง)<br />";
        
        // แสดงรูปภาพ (ต้องมีไฟล์ชื่อ 1.jpg อยู่ในโฟลเดอร์เดียวกัน)
        echo "<img src='download.jpg' width='200'> <hr>";
        
        $a++; // สำคัญมาก! ต้องเพิ่มค่าตัวแปรเพื่อไม่ให้เกิด Infinite Loop (วนซ้ำไม่จบสิ้น)
    }
    ?>

